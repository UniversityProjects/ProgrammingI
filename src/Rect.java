
public class Rect {

	public static void main (String[] args) {

		double base;
		double altezza;
		double perimetro;
		double area;
		
		base = 10;
		altezza = 5;
		
		perimetro = 2*(base+altezza);
		area = base*altezza;

		System.out.println ("Il perimetro del rettangolo misura " + perimetro + " cm" );
		System.out.println ("L'area del rettangolo e' " + area + " cm^2" );

	}

}

